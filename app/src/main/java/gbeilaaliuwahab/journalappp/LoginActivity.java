    public void signIn(){

        // Store values at the time of the login attempt.
        String email = mEmailView.getText().toString();
        String password = mPasswordView.getText().toString();

        // Check for a valid password, if the user entered one.
        if (!TextUtils.isEmpty(password) && !isPasswordValid(password)) {

            Toast.makeText(LoginActivity.this, "Email Empty and or Password Invalid!",
                    Toast.LENGTH_SHORT).show();
            return;
        } else if (TextUtils.isEmpty(email)) {
            Toast.makeText(LoginActivity.this, "Email is Empty!",
                    Toast.LENGTH_SHORT).show();
        } else if (!isEmailValid(email)) {

            Toast.makeText(LoginActivity.this, "Email is Empty!",
                    Toast.LENGTH_SHORT).show();
        }else {

            mAuth.signInWithEmailAndPassword(email, password)
                    .addOnCompleteListener(this, new OnCompleteListener<AuthResult>() {
                        @Override
                        public void onComplete(@NonNull Task<AuthResult> task) {
                            if (task.isSuccessful()) {
                                FirebaseUser user = mAuth.getCurrentUser();
                                Log.w("LOGINPASSED", user.getEmail());
                                updateUI(user);
                            } else {
                                // If sign in fails, display a message to the user.
                                Log.w("LOGINFAILED", "signInWithEmail:failure", task.getException());
                                Toast.makeText(LoginActivity.this, "Authentication failed.",
                                        Toast.LENGTH_SHORT).show();
                                updateUI(null);
                            }

                        }
                    });

        }
    }



    public void createAccount(String email, String password){

        mAuth.createUserWithEmailAndPassword(email, password)
                .addOnCompleteListener(this, new OnCompleteListener<AuthResult>() {
                    @Override
                    public void onComplete(@NonNull Task<AuthResult> task) {
                        if (task.isSuccessful()) {
                            // Sign in success, update UI with the signed-in user's information
                            Log.d("CREATEPASS", "createUserWithEmail:success");
                            FirebaseUser user = mAuth.getCurrentUser();
                            updateUI(user);

                        } else {
                            // If sign in fails, display a message to the user.
                            Log.w("CREATEFAILED", "createUserWithEmail:failure", task.getException());
                            Toast.makeText(LoginActivity.this, "Authentication failed.",
                                    Toast.LENGTH_SHORT).show();
                            updateUI(null);
                        }

                        // ...
                    }
                });

    }





    public void updateUI(FirebaseUser user) {

        if (user != null) {

            storeLoginUserDetailsInPreferences(user.getEmail());
            startActivity(new Intent(this,MainActivity.class));
            return;
        } else {

            // Sign in success, update UI with the signed-in user's information
            Log.d("LOGIN FAILED", "signInWithEmail:success");
            startActivity(new Intent(this,MainActivity.class));
            Toast.makeText(LoginActivity.this, "Authentication failed.",
                    Toast.LENGTH_SHORT).show();
            return;

        }
    }




    public void storeLoginUserDetailsInPreferences(String email){

        new EasyPrefs(getApplicationContext())
                .setPreference()
                .setKey("email")
                .setValue(email)
                .put();

    }
















